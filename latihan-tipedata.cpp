#include <iostream>

using namespace std;

int main() {
    string nama;
    string jurusan;
    int umur;
    float tinggibadan;

    cout << "Masukkan nama          : ";
    getline(cin, nama);

    cout << "Masukkan jurusan       : ";
    getline(cin, jurusan);

    cout << "Masukkan umur          : ";
    cin >> umur;

    cout << "Masukkan tinggi badan  : ";
    cin >> tinggibadan;

    cout << "\n======\n";
    cout << "Nama          : " << nama << endl;
    cout << "Jurusan       : " << jurusan << endl;
    cout << "Umur          : " << umur << " tahun" << endl;
    cout << "Tinggi Badan  : " << tinggibadan << " cm" << endl;

    return 0;
}
